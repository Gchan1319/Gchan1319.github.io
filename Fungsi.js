function playMusic(){
  let item = ["sound/bokuHero2.mp3","sound/Tensura2.mp3","sound/Baku.mp3","sound/Isabella.mp3","sound/MyWar.mp3","sound/Sasangeyo.mp3"]
  var musik = item[Math.floor(Math.random() * item.length)]
  aud.src = musik;
  aud.muted=false;
  aud.loop=true;
  aud.play();
  btn1.style.display="none"
  if(musik == "sound/bokuHero2.mp3"){
    var musik = "Peace Sign - Boku No Hero Academia OP 2"
  }else if(musik == "sound/Tensura2.mp3"){
    var musik = "StoryTeller - Tensei Shitara Datta Slime OP 2"
  }else if(musik == "sound/Isabella.mp3"){
    var musik = "Isabella's Lullaby"
  }else if(musik == "sound/MyWar.mp3"){
    var musik = "My War - Shingeki no Kyojin S4 OP"
  }else if(musik == "sound/Sasangeyo.mp3"){
    var musik = "Shinzou wo Sasangeyo - Shingeki no Kyojin S2 OP"
  }else if(musik == "sound/Baku.mp3"){
    var musik = "Baku - Ikimonogatari"
  }
  song.innerHTML = musik
  a.style.transition="1s"
  a.style.display="block"
  a.innerHTML="Audio On"
  setTimeout(function(){
    a.style.display="none"
  },500)
}
